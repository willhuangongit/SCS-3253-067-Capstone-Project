def preprocess_synthetic_data(alerts_dir: str, tr_dir: str) -> tuple:

    """
    Load the synthetic alerts dataset and transaction dataset, assign appropriate data types
    to each column, engineer additional features and return them.

    Parameters
    ----------
    alerts_dir, tr_dir: str
      The full directories of the alerts and transaction datasets.
      Both should be .csv files.

    Returns
    -------
    A 2-entry tuple containing pandas.DataFrame objects.
    The first entry is the preprocessed alerts dataset, and
    the second entry is the preprocessed transaction dataset.
    """

    # Load data while setting the alertID as the index.
    alerts = pd.read_csv(alerts_dir, index_col = 'AlertID')
    transactions = pd.read_csv(tr_dir)

    # Convert data to appropriate types.

    alerts = alerts.astype(
        {
            'Date': 'datetime64[ns]',
            'Outcome': 'category',
        }
    )

    transactions = transactions.astype(
        {
            'Timestamp': 'datetime64[ns]',
            'Entry': 'category',
            'Type': 'category',
            'Size': 'float64'
        }
    )

    # Sort the transaction records by alert ID and timestamp in ascending order.
    transactions = transactions.sort_values(by = ['AlertID', 'Timestamp'])

    # Identify the year-month of each alert.
    alerts['YearMonth'] = alerts['Date'].dt.to_period('M')

    # Specify all the known categories, even if some categories are not observed in the dataset.
    alerts['Outcome'] = alerts['Outcome'].cat.set_categories(['Report', 'Dismiss'])
    transactions['Entry'] = transactions['Entry'].cat.set_categories(['Credit', 'Debit'])
    transactions['Type'] = transactions['Type'].cat.set_categories(['Card', 'Cash', 'International', 'Wire'])

    # ===== ===== ===== ===== ===== ===== ===== =====
    # Feature Engineering
    # ===== ===== ===== ===== ===== ===== ===== =====

    # ----- ----- ----- ----- ----- ----- ----- -----
    # Transaction-Level Features
    # ----- ----- ----- ----- ----- ----- ----- -----

    # Create concatenated strings of all the defined entry and type categories.
    entry_type_categories = [
        f'{e}_{t}'
        for e, t in itertools.product(
          transactions['Entry'].cat.categories,
          transactions['Type'].cat.categories
        )
    ]

    # Concatenate the strings for entry and type.
    transactions['Entry_Type'] = pd.Categorical(
        values = transactions.apply(
          lambda row: f"{row['Entry']}_{row['Type']}",
          axis = 1
      ),
      categories = entry_type_categories
    )

    # ----- ----- ----- ----- ----- ----- ----- -----
    # Alert-Level Features
    # ----- ----- ----- ----- ----- ----- ----- -----

    """
    
    As described in the paper:
    
    > *For confidentiality, [...] we replace the date that any synthetic alert is “raised” with a random date from the same quarter (all dates in the quarter having equal probability).*
    
    As a result, the authors emphasize that the alert dates in the dataset are only meaningful at a quarterly frequency:
    
    > *[...] we stress that the synthetic alert dates only are accurate up to a quarterly division: any train-test split of the data should respect this (i.e., splits should be made either January the 1st, April the 1st, July the 1st, or October the 1st).*
    
    As a result, it is necessary to extract the year and quarter of each transaction and work with these, not the full alert date.
    """

    alerts[['Year', 'Quarter']] = alerts['Date'].apply(lambda d: pd.Series([d.year, d.quarter]))

    # Make sure the year and quarter have the desired data type.
    alerts = alerts.astype({'Year': 'int64'})
    alerts['Quarter'] = pd.Categorical(
        alerts['Quarter'],
        categories = [1,2,3,4],
        ordered = True
    )

    """## Number of Transactions per Alert"""

    num_tx = transactions.groupby('AlertID').size()
    num_tx.name = 'NumTx'
    alerts = pd.merge(left = alerts, right = num_tx, on = 'AlertID', how = 'left')

    """## Number of Transactions by Type"""

    # Count by transaction type.
    # Setting observed = False ensures that all the defined transaction types (categories)
    # will show up, even if they are not observed.
    transaction_types = transactions.groupby(['AlertID', 'Type'], observed = False).size()
    transaction_types = transaction_types.unstack(level = 'Type', fill_value=0)
    transaction_types = transaction_types.add_prefix('n_')

    # Merge the transaction types counts to the alerts dataset.
    alerts = pd.merge(left = alerts, right = transaction_types, on = 'AlertID', how = 'left')

    """## Number of Transactions by Entry"""

    credit_debit_counts = transactions.groupby(['AlertID', 'Entry'], observed = False).size()
    credit_debit_counts = credit_debit_counts.unstack(fill_value=0)
    credit_debit_counts.rename(columns = {'Credit': 'n_Credit', 'Debit': 'n_Debit'}, inplace = True)
    alerts = pd.merge(left = alerts, right = credit_debit_counts, on = 'AlertID', how = 'left')
    alerts[['n_Credit', 'n_Debit']] = alerts[['n_Credit', 'n_Debit']].fillna(0)

    """## Transactions by Entry crossed with Type"""

    # Count the cross between Type and Entry
    type_entry_counts = transactions.groupby(['AlertID', 'Entry_Type'], observed = False).size()
    type_entry_counts = type_entry_counts.unstack(level = 'Entry_Type', fill_value = 0)
    type_entry_counts = type_entry_counts.add_prefix('n_')
    alerts = pd.merge(left = alerts, right = type_entry_counts, on = 'AlertID', how = 'left')
    alerts[type_entry_counts.columns] = alerts[type_entry_counts.columns].fillna(0)

    """# Create features based on basic summary stats about transaction size"""

    # Calculate basic summary stats about transaction sizes for each alert ID.
    tr_size_stats = transactions.groupby('AlertID')['Size'].describe()

    # Drop counts from the summary stats as they are already available.
    tr_size_stats = tr_size_stats.drop('count', axis = 1)

    # Provide a prefix to the stat names to indicate transaction size.
    tr_size_stats = tr_size_stats.add_prefix('Size_')

    # Merge the stats to the alerts dataframe.
    alerts = pd.merge(left = alerts, right = tr_size_stats, on = 'AlertID', how = 'left')

    """# Create features based on basic summary stats about transaction timestamp"""

    # Create an ad-hoc function for calculating summary stats.
    # Although pandas has a default method describe() for doing so, this is done for these reasons:
    # 1. It avoids counting the number of observations, which has already been done before.
    # 2. describe() cannot calculate mean, standard deviation, etc. with datetime.
    # 3. It will be re-used several times later.

    def _get_summary_stats(s: pd.Series, ddof = 0, prefix = None, suffix = None):

      output = pd.Series({
              'min': s.min(),
              'max': s.max(),
              'mean': s.mean(),
              'std': s.std(ddof = ddof),
              '25%': s.quantile(q = 0.25),
              'median': s.quantile(q = 0.5),
              '75%': s.quantile(q = 0.75),
          }
      )

      if prefix:
        output = output.add_prefix(prefix)
      if suffix:
        output = output.add_suffix(suffix)

      return output

    # Summarize the timestamps by alert ID.
    ts_summary = transactions.groupby('AlertID')['Timestamp'].apply(_get_summary_stats, prefix = 'Timestamp_')
    ts_summary = ts_summary.unstack(level = -1)

    # Calculate the total time elapsed.
    ts_summary['Total_Time_Elapsed'] = ts_summary['Timestamp_max'] - ts_summary['Timestamp_min']

    # Merge the results to the alerts dataset.
    alerts = pd.merge(
        left = alerts,
        right = ts_summary,
        on = 'AlertID',
        how = 'left'
        )

    """# Create features based on basic summary stats about transaction size, grouped by different combinations of entry and type
    
    **Group by entry**
    """

    # Calculate basic summary stats about transaction sizes.
    # Because type is a pandas.Categorical data type, they are grouped in a separate step in order to always show
    # all the defined categories, even if some categories are not observed.
    tr_size_stats_by_entry = transactions.groupby('AlertID').apply(lambda df: df.groupby('Entry')['Size'].describe())

    # Turn counts to integers.
    tr_size_stats_by_entry = tr_size_stats_by_entry.astype({'count': int})

    # Move the types from the index to columns.
    tr_size_stats_by_entry = tr_size_stats_by_entry.unstack(level = 'Entry')

    # Flatten the column headers by concatenating the column names at different levels.
    tr_size_stats_by_entry.columns = [f'{entry}_Size_{stat}' for stat, entry in tr_size_stats_by_entry.columns]

    # Concatenate the results to the alerts dataframe.
    alerts = pd.concat([alerts, tr_size_stats_by_entry], axis = 1)

    """**Group by type**"""

    # Calculate basic summary stats about transaction sizes.
    # Because type is a pandas.Categorical data type, they are grouped in a separate step in order to always show
    # all the defined categories, even if some categories are not observed.
    tr_size_stats_by_type = transactions.groupby('AlertID').apply(lambda df: df.groupby('Type')['Size'].describe())

    # Turn counts to integers.
    tr_size_stats_by_type = tr_size_stats_by_type.astype({'count': int})

    # Move the entries from the index to columns.
    tr_size_stats_by_type = tr_size_stats_by_type.unstack(level = 'Type')

    # Flatten the column headers by concatenating the column names at different levels.
    tr_size_stats_by_type.columns = [f'{tr_type}_Size_{stat}' for stat, tr_type in tr_size_stats_by_type.columns]

    # Concatenate the results to the alerts dataframe.
    alerts = pd.concat([alerts, tr_size_stats_by_type], axis = 1)

    """**Group by entry and type**"""

    # Calculate basic summary stats about transaction sizes.
    # Because type is a pandas.Categorical data type, they are grouped in a separate step in order to always show
    # all the defined categories, even if some categories are not observed.
    tr_size_stats_by_entry_and_type = transactions.groupby('AlertID').apply(lambda df: df.groupby('Entry_Type')['Size'].describe())

    # Turn counts to integers.
    tr_size_stats_by_entry_and_type = tr_size_stats_by_entry_and_type.astype({'count': int})

    # Move the entries and types from the index to columns.
    tr_size_stats_by_entry_and_type = tr_size_stats_by_entry_and_type.unstack(level = ['Entry_Type'])

    # Flatten the column headers by concatenating the column names at different levels.
    tr_size_stats_by_entry_and_type.columns = [f'{entry_type}_Size_{stat}' for stat, entry_type in tr_size_stats_by_entry_and_type.columns]

    # Concatenate the results to the alerts dataframe.
    alerts = pd.concat([alerts, tr_size_stats_by_entry_and_type], axis = 1)

    """# Identify the Number of Days Prior to Alert
    
    For each alert and all the associated transaction history, calculate the number of days between each transaction timestamp and the alert date.
    """

    # Calculate the number of days between each timestamp and its corresponding alert date.
    transactions['Days Before Alert Date'] = (transactions['AlertID'].map(alerts['Date']) - transactions['Timestamp']).dt.days

    # Make the counts into categorical variables in case some days are unobserved.
    transactions['Days Before Alert Date'] = pd.Categorical(
        transactions['Days Before Alert Date'],
        categories = range(0, 365),
        ordered = True
    )

    """# Calculate the Total Exponential Size by Day
    
    This feature captures the transaction amount over time. Note that the feature "Size" in the original dataset is logged and standardized, so it has a mix of positive and negative values that generally sum up to zero. The authors of the dataset used log to suppress outliers. To detect the presence outliers for AML detection, however, take the exponent of the sizes to 1) make the outliers stand out more; 2) allow adding all the sizes without having positive and negative values cancel out.
    """

    # Take the exponent of the sizes.
    transactions['Size_Exp'] = transactions['Size'].apply(lambda x: exp(x))

    # Add the exponential sizes by alert ID and the number of days before alert date.
    total_size_exp_by_day = transactions.groupby('AlertID').apply(
        lambda df: df.groupby('Days Before Alert Date')['Size_Exp'].sum()
    )

    # Add a prefix to the column names.
    total_size_exp_by_day = total_size_exp_by_day.add_prefix('Size_Exp_Day_')

    # Concatenate the results to the alerts dataset.
    alerts = pd.concat([alerts, total_size_exp_by_day], axis = 1)

    """# Count the categorical variables by timestamp bins
    
    To carry the information on how categorical observations (such as entry and type) are distributed across time, bin these counts by timestamp.
    
    According to the authors of the dataset:
    
    > For every alert, we collect all transactions made by the underlying client up to 365 days before the alert was raised.
    
    A natural approach would then to bin the timestamps into 365 days before the alert was raised.
    
    **Count by Time and Entry**
    """

    # For each alert ID, count the number of transactions by entry and the number of days it took place before the alert occurs.
    # Note: The code below does not include AlertID in the "subset" argument in the value_count() method, because AlertID is not
    # a categorical variable, but 'Entry' and 'Days Before Alert Date' are. Instead, it's been moved to the groupby() method.
    # This coding approach will ensure that unobserved categories will always be counted in the final outcome (with a zero count).
    count_days_by_entry = transactions.groupby('AlertID').value_counts(subset = ['Entry', 'Days Before Alert Date'])
    count_days_by_entry.sort_index(ascending = True, inplace = True)

    # Move 'Entry' and 'Days Before Alert Date' from row indices to column names.
    count_days_by_entry = count_days_by_entry.unstack(level = ['Entry', 'Days Before Alert Date'])

    # Flatten the column levels by concatenating their names.
    count_days_by_entry.columns = [f'{name}_day_{days}' for name, days in count_days_by_entry.columns]

    # Concatenate the results to the 'alerts' dataframe.
    alerts = pd.concat([alerts, count_days_by_entry], axis = 1)

    """**Count by Time and Type**"""

    # For each alert ID, count the number of transactions by type and the number of days it took place before the alert occurs.
    count_days_by_type = transactions.groupby('AlertID').value_counts(subset = ['Type', 'Days Before Alert Date'])
    count_days_by_type.sort_index(ascending = True, inplace = True)

    # Move 'Type' and 'Days Before Alert Date' from row indices to column names.
    count_days_by_type = count_days_by_type.unstack(level = ['Type', 'Days Before Alert Date'])

    # Flatten the column levels by concatenating their names.
    count_days_by_type.columns = [f'{name}_day_{days}' for name, days in count_days_by_type.columns]

    # Concatenate the results to the 'alerts' dataframe.
    alerts = pd.concat([alerts, count_days_by_type], axis = 1)

    # ===== ===== ===== ===== ===== ===== ===== =====
    # Finalize
    # ===== ===== ===== ===== ===== ===== ===== =====
    return alerts, transactions


